    private void txtMontoGastoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMontoGastoKeyTyped
        char validar= evt.getKeyChar();
      if(Character.isLetter(validar)){
          getToolkit().beep();
          evt.consume();
          JOptionPane.showMessageDialog((rootPane), "Ingresar solo NÃºmeros");
      }
       if(txtMontoGasto.getText().length()>=7)
        {
             getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog((rootPane), "Solo se permite 7 digitos");
        }
      
    }//GEN-LAST:event_txtMontoGastoKeyTyped

