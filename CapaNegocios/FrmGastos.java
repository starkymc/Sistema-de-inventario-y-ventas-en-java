    private void btnBuscarGastoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarGastoActionPerformed
       try{ ConstructorGastos oGastos;
        int dato=Integer.parseInt(JOptionPane.showInputDialog("Codigo a buscar "));
        oGastos=oGastoDAO.buscarGastos(dato);
        if(oGastos==null){
            mensaje("CODIGO NO EXISTE");
            limpiarGastos();
            activarBotonesGastos(false,false,false,false);
            activarCajasGastos(false);
        }else{
            txtIdGasto.setText(""+oGastos.getCodigo());
            txtGasto.setText(oGastos.getNombre());
            txtDescripcionGastos.setText(oGastos.getDescripcion());
            txtMontoGasto.setText(""+oGastos.getMonto());
           

            activarBotonesGastos(false,false,true,true);
            activarCajasGastos(true);
        }
        }catch(Exception ex){
           System.out.println("Buscar : "+ex.getMessage());
      }
    }//GEN-LAST:event_btnBuscarGastoActionPerformed

    private void btnNuevoGastoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoGastoActionPerformed
        try{ if(btnNuevoGasto.getText().equalsIgnoreCase("Nuevo")){
            limpiarGastos();
            activarBotonesGastos(true,false,false,false);
            activarCajasGastos(true);
            btnNuevoGasto.setText("Cancelar");
            this.txtIdGasto.setText(String.valueOf(id_auto()));
        }else {
            limpiarGastos();
            activarBotonesGastos(false,true,false,false);
            activarCajasGastos(false);
            btnNuevoGasto.setText("Nuevo");
           
            
        }}catch(Exception ex){
           System.out.println("Nuevo : "+ex.getMessage());
      }
    }//GEN-LAST:event_btnNuevoGastoActionPerformed

